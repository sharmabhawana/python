#WAP to illustrate iteration over the list and dictionary.
lst1=["C","Cpp","C#","Python"]
print("Element of list:")
for i in lst1:
    print(i)
d={"r":"red","g":"green","b":"blue","C":"Cyan"}
print("keys of dictionary:")
for key in  d.keys():
    print(key,end=" ")
print()
print("values of dictionary:")
for value in d.values():
    print(value,end=" ")
print()
print("items of dictionary:")
for item in d.items():
    print(item,end=" ")
print()
